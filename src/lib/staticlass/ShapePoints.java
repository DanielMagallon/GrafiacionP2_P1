    public static int[] getRectangleOf(Point2D[] shape)
    {
        double xMax=0,xMin=shape[0].x,yMax=0,yMin=shape[0].y;

        for(Point2D p : shape)
        {
            if(p.x<xMin)
                xMin = p.x;

            if(p.x>xMax)
                xMax = p.x;

            if(p.y<yMin)
                yMin=p.y;

            if(p.y>yMax)
                yMax = p.y;
        }

        return new int[]{(int)++xMin,(int)++xMax,(int)++yMin,(int)++yMax};
    }

    public static boolean isInRange(int[] points,int[] clickPoints)
    {
        return (clickPoints[0]>=points[0] && clickPoints[0]<=points[1]) &&
                (clickPoints[1]>=points[2] && clickPoints[1]<=points[3]);
    }

